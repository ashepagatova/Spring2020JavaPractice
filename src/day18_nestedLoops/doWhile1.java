package day18_nestedLoops;

public class doWhile1 {


    public static void main(String[] args) {

        int i = 5;

        do{
            System.out.println("Hello World");
        }while(i <= 1);  // false
        //     5 == 0  ==> false


        System.out.println("=======================================");


        while( i == 0 ){

            System.out.println("Hello Cybertek");

            i++;
        }



    }


}
